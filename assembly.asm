	ORG 0000H
	SJMP BASLA
	ORG 000BH
	LJMP TOISR
	ORG 0030H
			
BASLA:
	CLR A
	MOV DPTR, #03FFH
	MOV P3,#0FFH
	MOV P1, #0FFH
	MOV R3,#0FFH
	MOV R4,#4000H
	MOV TMOD,#02H

ZAMANLAMA:
	MOV TH0,#-250
	MOV TL0,#-250
	SETB TR0
	MOV IE, #82H
	
BAS:
	JB P1.0, BAS

CALIS:
	CLR A
	MOV DPTR, #03FFH
	MOV P3,#0FFH
	MOV P1, #0FFH
	CALL SAY
	MOV P2, A
	MOV A, R2
	CLR P3.0
	CALL GECIKME
	SETB P3.0
	CJNE A,#2,CALIS2

		
CALIS2:
	CALL SAY
	MOV P2, A
	MOV A, R2
	CLR P3.1
	CALL GECIKME
	SETB P3.1
	CJNE A,#3,CALIS3
	
CALIS3:
	CALL SAY
	MOV P2, A
	MOV A, R2
	CLR P3.2
	CALL GECIKME
	SETB P3.2
	CJNE A,#4,CALIS4
	
	
CALIS4:
	CALL SAY
	MOV P2, A
	MOV A, R2
	CLR P3.3
	CALL GECIKME
	SETB P3.3
	CJNE A,#5,CALIS4
	DJNZ R3,CALIS	
	MOV P2,#00H
	SJMP BAS


GECIKME:
	MOV R0,#17
	MOV R1,#17
	BEKLE:
	DJNZ R0,BEKLE 
	MOV R0,#17
	DJNZ R1,BEKLE 
	RET

TOISR:
	 DJNZ R4,ZAMANLAMA
	 RETI

SAY: 
	INC A
	MOV R2, A  
	MOVC A,@A+DPTR	
	RET
	ORG 0400H 
TABLO:
	DB   05FH, 05CH, 06H, 5FH
	END